package day7;

public class review_task3_swichstatement {
	public static void main(String[] args) {
		int day=8;
		
		
		//swich-case 
		
		switch(day) {
		case(1):
			System.out.println("Monday,working day");
		break;
		case(2):
		System.out.println("Tuesday,working day");
		break;
		case(3):
			System.out.println("Wednesday,working day");
		break;
		case(4):
			System.out.println("thursday,working day");
		break;
		case(5):
			System.out.println("friday,working day");
		break;
		case(6):
			System.out.println("you are off today");
		break;
		case(7):
			System.out.println("you are off today");
		break;
		default :
			System.out.println("Invalid");
		
		
		}
	}

}
