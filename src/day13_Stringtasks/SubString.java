package day13_Stringtasks;

public class SubString {

	public static void main(String[] args) {
		
//Returns a new String by copying characters from an existing String.
				//	 0123456789
		String word="Television";
System.out.println(word.substring(2));
System.out.println(word.substring(4));//print letters after 4(v))
System.out.println(word.substring(1,4));// print letters between 1 and 4(e,l,e)not the v 
System.out.println(word.substring(4,10));//if you want to get last one you have to add 1 to the last index number 
System.out.println("bob".substring(9));//error out of bound





	
	
	
	}

}
